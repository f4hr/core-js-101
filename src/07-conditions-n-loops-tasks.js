/**
 * Returns the common directory path for specified array of full filenames.
 *
 * @param {array} paths
 * @return {string}
 *
 * @example:
 *   ['/web/images/image1.png', '/web/images/image2.png']  => '/web/images/'
 *   ['/web/assets/style.css', '/web/scripts/app.js',  'home/setting.conf'] => ''
 *   ['/web/assets/style.css', '/.bin/mocha',  '/read.me'] => '/'
 *   ['/web/favicon.ico', '/web-scripts/dump', '/verbalizer/logs'] => '/'
 */
function getCommonDirectoryPath(paths) {
  const pathsComponents = paths.map((p) => p.split('/'));
  const commonDir = [];

  for (let i = 0; i < pathsComponents[0].length - 1; i += 1) {
    const curr = pathsComponents[0][i];
    if (pathsComponents.every((p) => p[i] === curr)) {
      commonDir.push(curr);
    } else {
      break;
    }
  }

  return commonDir.length > 0 ? `${commonDir.join('/')}/` : '';
}


/**
 * Returns the product of two specified matrixes.
 * See details: https://en.wikipedia.org/wiki/Matrix_multiplication
 *
 * @param {array} m1
 * @param {array} m2
 * @return {array}
 *
 * @example:
 *   [[ 1, 0, 0 ],       [[ 1, 2, 3 ],           [[ 1, 2, 3 ],
 *    [ 0, 1, 0 ],   X    [ 4, 5, 6 ],     =>     [ 4, 5, 6 ],
 *    [ 0, 0, 1 ]]        [ 7, 8, 9 ]]            [ 7, 8, 9 ]]
 *
 *                        [[ 4 ],
 *   [[ 1, 2, 3]]    X     [ 5 ],          =>     [[ 32 ]]
 *                         [ 6 ]]
 *
 */
function getMatrixProduct(m1, m2) {
  const result = [];

  function product(row, col) {
    return m2.reduce((acc, _curr, i) => acc + m1[row][i] * m2[i][col], 0);
  }

  for (let i = 0; i < m1.length; i += 1) {
    const row = [];
    for (let j = 0; j < m2[0].length; j += 1) {
      row[j] = product(i, j);
    }
    result.push(row);
  }

  return result;
}


/**
 * Returns the evaluation of the specified tic-tac-toe position.
 * See the details: https://en.wikipedia.org/wiki/Tic-tac-toe
 *
 * Position is provides as 3x3 array with the following values: 'X','0', undefined
 * Function should return who is winner in the current position according to the game rules.
 * The result can be: 'X','0',undefined
 *
 * @param {array} position
 * @return {string}
 *
 * @example
 *
 *   [[ 'X',   ,'0' ],
 *    [    ,'X','0' ],       =>  'X'
 *    [    ,   ,'X' ]]
 *
 *   [[ '0','0','0' ],
 *    [    ,'X',    ],       =>  '0'
 *    [ 'X',   ,'X' ]]
 *
 *   [[ '0','X','0' ],
 *    [    ,'X',    ],       =>  undefined
 *    [ 'X','0','X' ]]
 *
 *   [[    ,   ,    ],
 *    [    ,   ,    ],       =>  undefined
 *    [    ,   ,    ]]
 *
 */
function evaluateTicTacToePosition(position) {
  function checkRows() {
    for (let i = 0; i < 3; i += 1) {
      if (position[i][0] === position[i][1] && position[i][1] === position[i][2]) {
        if (position[i][0] === 'X' || position[i][0] === '0') {
          return position[i][0];
        }
      }
    }
    return null;
  }

  function checkCols() {
    for (let i = 0; i < 3; i += 1) {
      if (position[0][i] === position[1][i] && position[1][i] === position[2][i]) {
        if (position[0][i] === 'X' || position[0][i] === '0') {
          return position[0][i];
        }
      }
    }
    return null;
  }

  function checkDiagonals() {
    if (position[0][0] === position[1][1] && position[1][1] === position[2][2]) {
      if (position[1][1] === 'X' || position[1][1] === '0') {
        return position[1][1];
      }
    }
    if (position[2][0] === position[1][1] && position[1][1] === position[0][2]) {
      if (position[1][1] === 'X' || position[1][1] === '0') {
        return position[1][1];
      }
    }
    return null;
  }

  const rows = checkRows();
  if (rows) {
    return rows;
  }
  const cols = checkCols();
  if (cols) {
    return cols;
  }
  const diagonals = checkDiagonals();
  if (diagonals) {
    return diagonals;
  }

  return undefined;
}


